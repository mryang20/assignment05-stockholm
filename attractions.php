		<h1>Klara kyrka</h1>

			<p>Close to the Sergels Torg square, and one of the few buildings in the district to survive the 1960s redevelopment, this large redbrick church was built in the 16th century, following the demolition of a 13th-century nunnery. The 116-metre steeple is the second tallest in Scandinavia and one of the ten tallest building in Sweden, making it a significant landmark. The artwork inside includes an 18th-century altarpiece. In the cemetery, a stone commemorates the 18th-century songwriter Carl Michael Bellman.</p>

		<h1>Museum of Modern Art (Moderna Museet)</h1>

			<p>Stockholm's museum of modern art is headed by Lars Nittve, formerly of London's Tate Modern. These museums have several works by famous artists - Pablo Picasso, Henri Mattise, Salvador Dalí and Irving Penn, an American photographer. Although its Stockholm counterpart might not have as vast a collection, there is still enough to satisfy both the modern art buff as well as the curious amateur. Also, the building, by Spanish architect Rafael Moneo, is a sight in itself.</p>

		<h1>National Museum (Nationalmuseum)</h1>
			<p>For those more interested in classical art, Nationalmuseum offers pieces by Rembrandt, Rubens, Goya, Renoir, Degas and Gauguin, as well as well-known Swedish artists such as Carl Larsson, Ernst Josephson, C F Hill and Anders Zorn. The museum also has a collection of applied art, design, and industrial design. The museum is situated in a beautiful 19th century building and has a nice café in its atrium.</p>

		<h1>Stockholm Public Library (Stadsbiblioteket)</h1>

			<p>Built in 1928 and designed by the famous Swedish architect Erik Gunnar Asplund, the interior of the cylinder-shaped main hall is spectacular, with three floors of bookshelves covering 360 degrees of circular wall. Books (both fiction and non-fiction) are available in many different languages, including English and German. On the cliff overlooking it is the old Observatory, which has a fine view of the city to the east. There is also a small café, and an Observatory Museum.</p>

